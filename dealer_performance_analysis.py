import pandas as pd
import numpy as np

# ==========================================
# 1. 데이터 불러오기 및 결측치(NaN) 정제
# ==========================================
# 인코딩이 utf-8-sig로 저장되었으니 읽을 때도 맞춰줍니다.
df = pd.read_csv('mock_message_data.csv', encoding='utf-8-sig')

print("--- [1] 원본 데이터 누락(NaN) 개수 확인 ---")
print(df.isnull().sum())
print("-" * 40)

# 실무 팁: 요청건수가 비어있으면(NaN) 합계나 평균 계산 시 오류가 나거나 제외되므로 0으로 채웁니다.
df['요청건수'] = df['요청건수'].fillna(0)


# ==========================================
# 2. 딜러사별 주요 지표 요약 (지표 분석 업무)
# ==========================================
# groupby를 사용해 각 딜러사별로 총 요청건수, 성공건수, 매출액의 합계를 구합니다.
dealer_summary = df.groupby('딜러사명')[['요청건수', '성공건수', '매출액']].sum().reset_index()

# 파생 변수 생성: 실무 리포트의 꽃인 '발송 성공률' 지표를 추가합니다.
# (0으로 나누는 에러를 방지하기 위해 np.where 또는 단순 연산 사용)
dealer_summary['발송성공률(%)'] = (dealer_summary['성공건수'] / dealer_summary['요청건수']) * 100
dealer_summary['발송성공률(%)'] = dealer_summary['발송성공률(%)'].round(2) # 소수점 둘째자리 반올림

print("--- [2] 딜러사별 발송 지표 요약 결과 ---")
print(dealer_summary)
print("-" * 40)


# ==========================================
# 3. 메시지 유형별 피벗 테이블 (엑셀 보고서 자동화)
# ==========================================
# 엑셀의 피벗 테이블 기능을 코드 한 줄로 구현합니다.
# 어떤 딜러사가 어떤 메시지(알림톡, 친구톡 등)를 주로 쓰는지 한눈에 보여줍니다.
pivot_result = pd.pivot_table(
    df,
    values='성공건수',      # 표 안에 들어갈 값
    index='딜러사명',      # 세로 축 (행)
    columns='메시지유형',   # 가로 축 (열)
    aggfunc='sum',        # 집계 방식 (합계)
    fill_value=0          # 데이터가 없는 빈칸은 0으로 채움
)

print("--- [3] 메시지 유형별 피벗 테이블 결과 ---")
print(pivot_result)
print("-" * 40)


# ==========================================
# 4. 특정 조건 필터링 및 엑셀 저장 (딜러사 지원/공문 양식)
# ==========================================
# 조건: 매출액이 2,500만 원 이상인 우수 딜러사만 추출하거나 
# 반대로 실패율이 높아 컨설팅이 필요한 딜러사 목록을 뽑을 때 쓰는 방식입니다.
high_sales_dealers = dealer_summary[dealer_summary['매출액'] >= 25000000]

print("--- [4] 매출 2,500만 이상 집중 관리 대상 딜러사 ---")
print(high_sales_dealers[['딜러사명', '매출액']])
print("-" * 40)

# 최종 보고용 엑셀 파일로 깔끔하게 저장하며 마무리!
with pd.ExcelWriter('카카오_비즈메시지_최종보고서.xlsx') as writer:
    dealer_summary.to_excel(writer, sheet_name='딜러사별_종합지표', index=False)
    pivot_result.to_excel(writer, sheet_name='메시지유형별_현황')

print("🎉 모든 분석이 완료되어 '카카오_비즈메시지_최종보고서.xlsx' 파일로 저장되었습니다!")
